const { ChessJSAdapter } = require('../js/chessjs-adapter.js');
const { Chess } = require('chess.js');
const assert = require('assert');

console.log('🧪 ChessJSAdapter Compatibility Tests');

const testPositions = [
  {
    name: 'Start Position',
    fen: 'rnbqkbnr/pppppppp/8/8/8/8/PPPPPPPP/RNBQKBNR w KQkq - 0 1',
    expectedMoves: 20
  },
  {
    name: 'Tricky Position 1',
    fen: 'r3k2r/p1ppqpb1/bn2pnp1/3PN3/1p2P3/2N2Q1p/PPPBBPPP/R3K2R w KQkq - 0 1',
    expectedMoves: 48
  },
  {
    name: 'Killer Position',
    fen: 'rnbq1k1r/pp1Pbppp/2p5/8/2B5/8/PPP1NnPP/RNBQK2R w KQ - 1 8',
    expectedMoves: 44
  }
];

testPositions.forEach(pos => {
  testAdapterVsChess(pos);
});

function testAdapterVsChess(pos) {
  console.log(`Testing: ${pos.name}`);
  
  const adapter = new ChessJSAdapter(pos.fen);
  const chessRef = new Chess(pos.fen);

  // 1. Move count match
  const adapterMoves = adapter.moves();
  const chessMoves = chessRef.moves();
  assert.strictEqual(
    adapterMoves.length, 
    pos.expectedMoves, 
    `Move count mismatch: expected ${pos.expectedMoves}, got ${adapterMoves.length}`
  );
  console.log(`  ✓ Moves: ${adapterMoves.length} (expected ${pos.expectedMoves})`);

  // 2. FEN match
  assert.strictEqual(adapter.fen(), chessRef.fen(), 'Initial FEN mismatch');

  // 3. Turn match
  assert.strictEqual(adapter.turn(), chessRef.turn(), 'Turn mismatch');

  // 4. Check detection (using modern API)
  assert.strictEqual(adapter.isCheck(), chessRef.isCheck(), 'isCheck() mismatch');

  // 5. SAN move match
  // Compare a random move to see if SAN generation is correct
  if (adapterMoves.length > 0) {
    // Standardize: chess.js might have moves in different order, so we check if adapter moves are in chessRef moves
    adapterMoves.forEach(m => {
        assert.ok(chessMoves.includes(m), `Move ${m} generated by adapter not found in chess.js moves`);
    });
    console.log('  ✓ SAN generation match');

    const moveSAN = adapterMoves[0];
    const moveResult = adapter.move(moveSAN);
    const chessResult = chessRef.move(moveSAN);
    
    assert.ok(moveResult, `Adapter move failed: ${moveSAN}`);
    assert.ok(chessResult, `Chess.js move failed: ${moveSAN}`);
    assert.strictEqual(adapter.fen(), chessRef.fen(), `FEN mismatch after ${moveSAN}`);
    console.log(`  ✓ First move "${moveSAN}" OK`);
  }

  // 6. Undo test
  if (adapter.history().length > 0) {
    adapter.undo();
    chessRef.undo();
    assert.strictEqual(adapter.fen(), chessRef.fen(), 'Undo FEN mismatch');
    console.log('  ✓ Undo OK');
  }

  console.log(`  ${'✅'.repeat(6)} ${pos.name} PASSED\n`);
}

console.log('\n🎉 All ChessJSAdapter tests PASSED! 100% chess.js compatible\n');
