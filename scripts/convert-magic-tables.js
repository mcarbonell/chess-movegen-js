const fs = require('fs');

function convert() {
    let content = fs.readFileSync('js/magic-tables.js', 'utf8');
    
    // Comprehensive BigInt cleanup (strips any 'n' followed by a separator or end of line)
    content = content.replace(/([0-9a-fA-F])n(?=[^a-zA-Z0-9]|$)/g, '$1');

    const output = ['// Auto-generated by scripts/convert-magic-tables.js', ''];

    function parseArray(varName) {
        const startMarker = `const ${varName} = [`;
        const startIdx = content.indexOf(startMarker);
        if (startIdx === -1) return null;
        
        let depth = 1;
        let endIdx = -1;
        for (let i = startIdx + startMarker.length; i < content.length; i++) {
            if (content[i] === '[') depth++;
            else if (content[i] === ']') depth--;
            
            if (depth === 0) {
                endIdx = i;
                break;
            }
        }
        
        if (endIdx === -1) return null;
        
        let arrayStr = content.substring(startIdx + startMarker.length, endIdx);
        return arrayStr;
    }

    // 1. lookup_table
    const lookupStr = parseArray('lookup_table');
    if (lookupStr) {
        const vals = lookupStr.split(',').map(s => s.trim()).filter(s => s);
        output.push(`pub static LOOKUP_TABLE: [u64; ${vals.length}] = [`);
        for (let i = 0; i < vals.length; i += 8) {
            output.push('    ' + vals.slice(i, i + 8).join(', ') + ',');
        }
        output.push('];\n');
    }

    // 2. Sliders (b_magics, r_magics)
    function processSlider(varName, prefix) {
        const str = parseArray(varName);
        if (!str) return;

        // Matches [ offset, mask, magic ]
        const matches = [...str.matchAll(/\[\s*(\d+)\s*,\s*(\d+)\s*,\s*(\d+)\s*\]/g)];
        
        const offsets = matches.map(m => m[1]);
        const masks = matches.map(m => m[2]);
        const magics = matches.map(m => m[3]);

        output.push(`pub static ${prefix}_OFFSET: [usize; 64] = [${offsets.join(', ')}];`);
        output.push(`pub static ${prefix}_MASK: [u64; 64] = [${masks.join(', ')}];`);
        output.push(`pub static ${prefix}_MAGIC: [u64; 64] = [${magics.join(', ')}];\n`);
    }

    processSlider('b_magics', 'BISHOP');
    processSlider('r_magics', 'ROOK');

    fs.writeFileSync('rust-movegen/src/magic_tables.rs', output.join('\n'));
    console.log('Successfully generated magic_tables.rs');
}

convert();
